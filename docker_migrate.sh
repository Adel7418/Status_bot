if docker-compose up -d bot; then
    log_success "–ë–æ—Ç –∑–∞–ø—É—â–µ–Ω"
else
    log_error "–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–ø—É—Å—Ç–∏—Ç—å –±–æ—Ç–∞"
    exit 1
fi

# 7. –û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—É—Å–∫–∞
