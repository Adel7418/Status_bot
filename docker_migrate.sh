BACKUP_NAME="bot_database_backup_$(date +%Y%m%d_%H%M%S).db"

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, –∑–∞–ø—É—â–µ–Ω –ª–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
if docker-compose ps bot | grep -q "Up"; then
    # –ö–æ–Ω—Ç–µ–π–Ω–µ—Ä –∑–∞–ø—É—â–µ–Ω, —Å–æ–∑–¥–∞–µ–º –±—ç–∫–∞–ø –≤–Ω—É—Ç—Ä–∏
    if docker-compose exec bot cp /app/data/bot_database.db /app/data/$BACKUP_NAME; then
        log_success "–ë—ç–∫–∞–ø —Å–æ–∑–¥–∞–Ω –≤–Ω—É—Ç—Ä–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞: $BACKUP_NAME"
    else
        log_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ–∑–¥–∞—Ç—å –±—ç–∫–∞–ø –≤–Ω—É—Ç—Ä–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞"
        exit 1
    fi
