    # –ö–æ–Ω—Ç–µ–π–Ω–µ—Ä –Ω–µ –∑–∞–ø—É—â–µ–Ω, –ø—ã—Ç–∞–µ–º—Å—è —Å–∫–æ–ø–∏—Ä–æ–≤–∞—Ç—å —Ñ–∞–π–ª
    if [ -f "data/bot_database.db" ]; then
        cp data/bot_database.db data/$BACKUP_NAME
        log_success "–ë—ç–∫–∞–ø —Å–æ–∑–¥–∞–Ω –Ω–∞ —Ö–æ—Å—Ç–µ: data/$BACKUP_NAME"
    else
        log_warning "–§–∞–π–ª –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω. –ü—Ä–æ–¥–æ–ª–∂–∞–µ–º –±–µ–∑ –±—ç–∫–∞–ø–∞."
    fi
fi

# 2. –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –±–æ—Ç–∞
log_info "–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –±–æ—Ç–∞..."
if docker-compose stop bot; then
    log_success "–ë–æ—Ç –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω"
else
    log_warning "–ù–µ —É–¥–∞–ª–æ—Å—å –æ—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –±–æ—Ç–∞ (–≤–æ–∑–º–æ–∂–Ω–æ, —É–∂–µ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω)"
